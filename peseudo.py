# # 어떻게 만들 것인가? - 준비

# </aside>

# - Tkinter를 이용하여 구현함
#     - 결제
#         - 현금을 넣는 행위는 실제로 구현 가능하지 않기 때문에 새로운 창에서 버튼으로 수정
#             - 카드의 경우에는 버튼을 누르면 구입가능하도록 설정하겠음
#                 - 신용카드는 모든 상품 구입가능, 체크카드의 경우에는 1500원이 들어있다고 가정
#     - 상품
#         - 배열 {번호, 상품이름, 상품 사진, 가격, 재고}을 써야함 → 리스트 안에 딕셔너리로 구성
    
#     - 키패드
#         - 결정 버튼
#             - “감사합니다” 3초 띄우고 입력창 비우기
#             - 구입할 수 없는 상품을 골랐다면 “ 다시 입력해주십시오” 3초 띄우고 입력창 비우기
#         - 취소 버튼
#             - “취소했습니다” 3초 띄우고 입력창 비우기
        
#     - 상품 구입
#         - 현금 입력창에 ‘상품이 구입이 완료되었습니다’ 노출 → 3초 띄우고 입력창 비우기
#         - 4X5 형식으로 상품을 배치
#     - 상품 배출구
#         - 결정 완료를 누르면 상품을 노출
#         - 배출구 위치는 키패드 밑으로 하겠음
#     - 잔돈
#         - 자판기 내에 돈이 없다면 “[번호] 연락주십시오.” → 이것은 삭제 버튼을 누르는 것이 아니면 사라지지 않음
#         - 잔돈 배출구는 상품 배출구 옆으로 배치
#     - CURD 게시판
#         - 리스트 안 딕셔너리를 이용
#         - {아이디, 내용, 비밀번호, 관리자 비밀번호} 로 형성
#         - 삭제 시 되묻는 행위는 필요하지 않음
#         - 수정이 꼭 필요한가? → 자판기 특성상 삭제 후 다시 작성하는게 더 빠르다고 판단
        

